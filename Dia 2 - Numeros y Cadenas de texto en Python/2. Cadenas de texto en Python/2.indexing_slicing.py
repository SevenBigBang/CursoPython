#Indexing: Capacidad de acceder a elementos especificos o caracteres de una cadena de caracteres
cadena = "Hola, mundo!"
print(cadena[-1])


# Slicing: Extrar cademas mas grandes de mi cadena principal, aplica para, listas, tuplas etc...
libros = ["Hola", "Como", "Estas"]
print(libros[0:2])

cadena = "Hola, como estas?"
print(cadena[0:8])

tupla = ("Hola", "Bien", "y", "Usted")
print(tupla[0:3])


# slicing con saltos de dos en dos:
cadena = "Hola, mundo!"
print(cadena[::2])